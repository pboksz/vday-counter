public class MonthDayObject
{
   int month;
   long days;

   public int getMonth()
   {
      return month;
   }

   public void setMonth(int month)
   {
      this.month = month;
   }

   public long getDays()
   {
      return days;
   }

   public void setDays(long days)
   {
      this.days = days;
   }
}
